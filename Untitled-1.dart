function searchBooks(searchInput, selector) {
  const url = `/books?${selector}=${searchInput}`;

  fetch(url)
    .then(response => response.json())
    .then(data => {
      const tableBody = document.querySelector('#results-table tbody');
      tableBody.innerHTML = '';

      data.forEach(book => {
        const row = document.createElement('tr');
        row.innerHTML = `
          <td>${book.title}</td>
          <td>${book.author}</td>
          <td>${book.category}</td>
        `;
        tableBody.appendChild(row);
      });
    })
    .catch(error => console.error(error));
}


